                    <h2 id="surrendering-your-gst-registration">Introduction</h2>
                    <p>The Presumptive Taxation Scheme is a simplified way of calculating and filing your ITR. It is available to small businesses and professionals with a turnover of up to Rs 2 crores. Under this scheme, taxpayers can declare their income at a prescribed rate based on their profession or business type, and pay tax on that income without maintaining detailed books of accounts.</p>
                    <p>we will take you through the entire process of filing your ITR using the Presumptive Taxation Scheme. We will cover the eligibility criteria, the tax rates, and the documents required to file your return.</p>
                    <h3 id="eligibility-criteria-for-the-presumptive-taxation-scheme">Eligibility criteria for the Presumptive Taxation Scheme</h3>
                    <p class="fw-bold">To be eligible for the Presumptive Taxation Scheme, you must meet the following conditions:</p>
                    <ul>
                        <li>You must be a resident individual, Hindu Undivided Family (HUF), or partnership firm.</li>
                        <li>You must have a total turnover of up to Rs 2 crores in the financial year.</li>
                    </ul>
                    <p class="fw-bold">You must be engaged in any of the following businesses or professions:</p>
                    <ul>
                        <li>Retail trade</li>
                        <li>Civil construction</li>
                        <li>Transportation</li>
                        <li>Catering or hotel business</li>
                        <li>Professionals like doctors, lawyers, architects, and others</li>
                        <li>Tax rates under the Presumptive Taxation Scheme</li>
                    </ul>
                    <p class="fw-bold">The Presumptive Taxation Scheme has different tax rates for different professions and businesses. Here are the tax rates for the most common professions and businesses:</p>
                    <ul>
                        <li>For retail traders, the tax rate is 6% of the turnover.</li>
                        <li>For civil construction businesses, the tax rate is 8% of the turnover.</li>
                        <li>For transportation businesses, the tax rate is 8% of the turnover.</li>
                        <li>For catering or hotel businesses, the tax rate is 8% of the turnover.</li>
                        <li>For professionals like doctors, lawyers, architects, and others, the tax rate is 50% of the gross receipts.</li>
                    </ul>
                    <p class="fw-bold">To file your ITR using the Presumptive Taxation Scheme, you will need the following documents:</p>
                    <ul>
                        <li>A copy of your PAN card</li>
                        <li>A copy of your Aadhaar card</li>
                        <li>A copy of your bank statement</li>
                        <li>A copy of your turnover statement</li>
                        <li>A copy of your profit and loss statement</li>
                        <li>A copy of your balance sheet</li>
                    </ul>
                    <p class="fw-bold">Steps to file your ITR using the Presumptive Taxation Scheme. Here are the steps to file your ITR using the Presumptive Taxation Scheme:</p>
                    <ul>
                        <li>Visit the Income Tax Department website and log in with your credentials.</li>
                        <li>Click on the e-file tab and select "Income Tax Return."</li>
                        <li>Select the assessment year for which you are filing your return.</li>
                        <li>Select the ITR form applicable to you (ITR-3 for partnership firms and ITR-4 for individuals and HUFs).</li>
                        <li>Fill in the required details, such as personal information, income details, and tax payments.</li>
                        <li>Enter the turnover and tax details under the Presumptive Taxation Scheme.</li>
                        <li>Upload all the necessary documents and submit your return.</li>
                    </ul>
