          <h3 id="deductions-and-exemptions">Deductions and exemptions</h3>
          <p>Some of the most common deductions and exemptions for salaried individuals include:</p>
          <ul>
            <li><strong>House Rent Allowance (HRA):</strong> If you are required to live away from your home for work, you may be eligible to claim a deduction for your HRA. The amount of the deduction is limited to 50% of your HRA, or the actual rent paid, whichever is lower.</li>
            <li><strong>Medical Insurance Premium:</strong> You may be able to claim a deduction for the premium you pay for medical insurance for yourself, your spouse, and your dependent children. The maximum deduction is Rs. 25,000 per year.</li>
            <li><strong>Interest on Home Loan:</strong> If you have taken a home loan, you may be able to claim a deduction for the interest you pay on the loan. The maximum deduction is Rs. 2 lakh per year.</li>
            <li><strong>Pension Contributions:</strong> If you contribute to a pension scheme, you may be able to claim a deduction for your contributions. The maximum deduction is Rs. 1.5 lakh per year.</li>
            <li><strong>Donations to Charitable Organizations:</strong> You may be able to claim a deduction for donations you make to charitable organizations. The maximum deduction is 10% of your total income.</li>
          </ul>
