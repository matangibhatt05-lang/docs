          <ul>
            <li><strong>Filing of GST Returns:</strong> Registered businesses are required to file various GST returns such as GSTR-1 (for outward supplies), GSTR-3B (for monthly summary), GSTR-9 (annual return), etc. The frequency and due dates for filing returns depend on the turnover of the business.</li>
            <li><strong>Payment of GST:</strong> Registered businesses are required to pay GST on their supplies of goods or services. The payment of GST can be made online through the GST portal using the Electronic Cash Ledger.</li>
            <li><strong>Maintenance of Records:</strong> Registered businesses are required to maintain proper records of their inward and outward supplies, tax invoices, credit and debit notes, and other relevant documents.</li>
            <li><strong>Auditing:</strong> Businesses with a turnover of more than Rs. 2 crores must get their accounts audited by a chartered or cost accountant and submit an annual audit report.</li>
            <li><strong>Compliance with Anti-Profiteering Rules:</strong> Businesses are required to comply with the anti-profiteering rules under GST, which require them to pass on the benefits of GST rate reduction to the end consumer.</li>
